#include <stdio.h>

int main()
{
    for (int i = 1; i <= 100; i++)
    {
        printf("Henil \n");
    }

    for (int x = 0; x <= 100; x++)
    {
        printf("%d \n", x);
    }

    for (int a = 10; a >= 1; a = a - 1)
    {
        printf("%d \n", a);
    }

    return 0;
}

